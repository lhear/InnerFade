type trieNode struct {
	children []*trieNode
	mask     uint64
	code     uint16
	isEnd    bool
}

func (n *trieNode) addChild(charIdx int8) *trieNode {
	bit := uint64(1) << uint64(charIdx)
	if n.mask&bit != 0 {
		idx := bits.OnesCount64(n.mask & (bit - 1))
		return n.children[idx]
	}
	newNode := &trieNode{}
	idx := bits.OnesCount64(n.mask & (bit - 1))
	n.children = append(n.children, nil)
	if idx < len(n.children)-1 {
		copy(n.children[idx+1:], n.children[idx:])
	}
	n.children[idx] = newNode
	n.mask |= bit
	return newNode
}

type FlattenedNode struct {
	Mask         uint64
	ChildrenBase uint32
	Code         uint16
	IsEnd        bool
}

func main() {
	var charToIdxTable [256]int8
	for i := range 256 {
		charToIdxTable[i] = -1
	}
	var idxToCharTable [base]byte
	for i := range len(charset) {
		c := charset[i]
		idx := int8(i)
		charToIdxTable[c] = idx
		idxToCharTable[i] = c
		if c >= 'a' && c <= 'z' {
			charToIdxTable[c-32] = idx
		}
	}

	maxDictCapacity := 65536 - offsetDict
	if len(commonDict) > maxDictCapacity {
		log.Fatalf("Dictionary size (%d) exceeds limit", len(commonDict))
	}
	sortedDict := make([]string, len(commonDict))
	copy(sortedDict, commonDict)
	sort.Slice(sortedDict, func(i, j int) bool {
		return len(sortedDict[i]) > len(sortedDict[j])
	})

	root := &trieNode{}
	for i, s := range sortedDict {
		code := uint16(offsetDict + i)
		node := root
		for j := 0; j < len(s); j++ {
			charIdx := charToIdxTable[s[j]]
			if charIdx < 0 {
				continue
			}
			node = node.addChild(charIdx)
		}
		if !node.isEnd {
			node.isEnd = true
			node.code = code
		}
	}

	flatNodes := []FlattenedNode{}
	type queueItem struct {
		ptr *trieNode
		idx int
	}

	flatNodes = append(flatNodes, FlattenedNode{
		Mask:  root.mask,
		Code:  root.code,
		IsEnd: root.isEnd,
	})

	queue := []queueItem{{ptr: root, idx: 0}}

	head := 0
	for head < len(queue) {
		curr := queue[head]
		head++

		ptrNode := curr.ptr
		flatIdx := curr.idx

		if len(ptrNode.children) > 0 {
			childrenBase := len(flatNodes)
			flatNodes[flatIdx].ChildrenBase = uint32(childrenBase)

			for range ptrNode.children {
				flatNodes = append(flatNodes, FlattenedNode{})
			}

			for i, childPtr := range ptrNode.children {
				childIdx := childrenBase + i
				flatNodes[childIdx] = FlattenedNode{
					Mask:  childPtr.mask,
					Code:  childPtr.code,
					IsEnd: childPtr.isEnd,
				}
				queue = append(queue, queueItem{ptr: childPtr, idx: childIdx})
			}
		}
	}

	data := struct {
		CharToIdx [256]int8
		IdxToChar [base]byte
		DictList  []string
		TrieNodes []FlattenedNode
	}{
		CharToIdx: charToIdxTable,
		IdxToChar: idxToCharTable,
		DictList:  sortedDict,
		TrieNodes: flatNodes,
	}

	const tmpl = `package compress

// Code generated by gen.go; DO NOT EDIT.

type flatTrieNode struct {
	mask         uint64
	childrenBase uint32
	code         uint16
	isEnd        bool
}

var charToIdxTable = [256]int8{
{{- range $i, $v := .CharToIdx }}{{ if eq (mod $i 16) 0 }}
	{{ end }}{{ printf "%d, " $v }}{{ end }}
}

var idxToCharTable = [38]byte{
{{- range $i, $v := .IdxToChar }}{{ if eq (mod $i 16) 0 }}
	{{ end }}{{ printf "%d, " $v }}{{ end }}
}
	
var dictList = []string{
{{- range $v := .DictList }}	"{{ $v }}",
{{ end }}}


var trieNodes = [...]flatTrieNode{
{{- range $i, $n := .TrieNodes }}
	{mask: {{ printf "%d" $n.Mask }}, childrenBase: {{ $n.ChildrenBase }}, code: {{ $n.Code }}, isEnd: {{ $n.IsEnd }}},
{{- end }}
}
`

	funcMap := template.FuncMap{
		"mod": func(i, j int) int { return i % j },
	}

	t, err := template.New("tables").Funcs(funcMap).Parse(tmpl)
	if err != nil {
		log.Fatal(err)
	}

	var buf bytes.Buffer
	if err := t.Execute(&buf, data); err != nil {
		log.Fatal(err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatal("Format error:", err)
	}

	if err := os.WriteFile("tables_gen.go", formatted, 0644); err != nil {
		log.Fatal(err)
	}

	fmt.Println("Successfully generated tables_gen.go")
}
